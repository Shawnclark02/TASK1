class Arithmetic {
    public static void main(String[] args) {
        
        int result = 1 + 15;
        System.out.println(result);

        result = result - 6;
        System.out.println(result);

        result = result * 5;
        System.out.println(result);

        result = result / 3;
        System.out.println(result);

        result = result + 10;
        result = result - 15;
        result = result % 9;
        System.out.println(result);

    }
}
